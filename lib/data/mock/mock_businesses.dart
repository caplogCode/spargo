List<BusinessHours> _hours({
  required String weekdayOpen,
  required String weekdayClose,
  String saturdayOpen = '10:00',
  String saturdayClose = '18:00',
  bool sundayClosed = true,
}) {
  return <BusinessHours>[
    BusinessHours(day: 'Mo', opensAt: weekdayOpen, closesAt: weekdayClose),
    BusinessHours(day: 'Di', opensAt: weekdayOpen, closesAt: weekdayClose),
    BusinessHours(day: 'Mi', opensAt: weekdayOpen, closesAt: weekdayClose),
    BusinessHours(day: 'Do', opensAt: weekdayOpen, closesAt: weekdayClose),
    BusinessHours(day: 'Fr', opensAt: weekdayOpen, closesAt: weekdayClose),
    BusinessHours(day: 'Sa', opensAt: saturdayOpen, closesAt: saturdayClose),
    BusinessHours(
      day: 'So',
      opensAt: sundayClosed ? '-' : saturdayOpen,
      closesAt: sundayClosed ? '-' : saturdayClose,
      isClosed: sundayClosed,
    ),
  ];
}

